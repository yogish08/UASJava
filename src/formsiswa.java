    public void loadDataSiswa(){
        nisSiswa = txtNis.getText();
        nmSiswa = txtNama.getText();
        klsSiswa = (String) txtKelas.getSelectedItem();
        jurusanSiswa = (String) txtJurusan.getSelectedItem();
    }
    
    //memilih data saat baris pada tabel dipilih dengan mouse
    public void dataSelect(){
        //deklarasi variabel
        int i = tblSiswa.getSelectedRow();
        
        //uji adakah data di tabel?
        if(i == -1){
            //tidak ada yang terpilih atau dipilih.
            return;
        }
        txtNis.setText(""+model.getValueAt(i,0));
        txtNama.setText(""+model.getValueAt(i,1));
        txtKelas.setSelectedItem(""+model.getValueAt(i,2));
        txtJurusan.setSelectedItem(""+model.getValueAt(i,3));
    }
     
    //mengosongkan form
    public void reset(){
        nisSiswa = "";
        nmSiswa = "";
        klsSiswa = "";
        jurusanSiswa = "";
               
        txtNis.setText(nisSiswa);
        txtNama.setText(nmSiswa);
        txtKelas.setSelectedItem(klsSiswa);
        txtJurusan.setSelectedItem(jurusanSiswa);
    }
    
    public void simpanDataSiswa(){
        //panggil fungsi load data
        loadDataSiswa();
        
        //uji koneksi dan eksekusi perintah
        try{
            //test koneksi
            Statement stat = (Statement) koneksidb.getKoneksi().createStatement();
            
            //perintah sql untuk simpan data
            String  sql =   "INSERT INTO tbl_siswa(nis, nama_siswa, kelas, jurusan)" + "VALUES('"+ nisSiswa +"','"+ nmSiswa +"','"+ klsSiswa +"','"+ jurusanSiswa +"')";
            PreparedStatement p = (PreparedStatement) koneksidb.getKoneksi().prepareStatement(sql);
            p.executeUpdate();
            
            //ambil data
            getDataSiswa();
        }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }
    
    public void perbaruiDataSiswa(){
        //fungsi load data kategori
        loadDataSiswa();
        
        try{
            //uji koneksi
            Statement stat = (Statement) koneksidb.getKoneksi().createStatement();
            
            //kirim perintah sql
            String sql = "UPDATE tbl_siswa SET nama_siswa = '"+ nmSiswa +"' WHERE nis = '"+ nisSiswa +"'";
            PreparedStatement p =(PreparedStatement)koneksidb.getKoneksi().prepareStatement(sql);
            p.executeUpdate();
            
            //ambil data
            getDataSiswa();
            
            //kosongkan data
            reset();
            JOptionPane.showMessageDialog(null, "PERUBAHAN DATA BERHASIL");
        }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }
                                     
    public void hapusDataSiswa(){
        //panggil fungsi ambil data
        loadDataSiswa(); 
        
        //Beri peringatan sebelum melakukan penghapusan data
        int pesan = JOptionPane.showConfirmDialog(null, "HAPUS DATA"+ nisSiswa +"?","KONFIRMASI", JOptionPane.OK_CANCEL_OPTION);
        
        //jika pengguna memilih OK lanjutkan proses hapus data
        if(pesan == JOptionPane.OK_OPTION){
            //uji koneksi
            try{
                //buka koneksi ke database
                Statement stat = (Statement) koneksidb.getKoneksi().createStatement();
                
                //perintah hapus data
                String sql = "DELETE FROM tbl_siswa WHERE nis='"+ nisSiswa +"'";
                PreparedStatement p =(PreparedStatement)koneksidb.getKoneksi().prepareStatement(sql);
                p.executeUpdate();
                
                //fungsi ambil data
                getDataSiswa();
                
                //fungsi reset data
                reset();
                JOptionPane.showMessageDialog(null, "KENANGAN BERSAMANYA BERHASIL DIHAPUS");
            }catch(SQLException err){
                JOptionPane.showMessageDialog(null, err.getMessage());
            }
        }
    }
